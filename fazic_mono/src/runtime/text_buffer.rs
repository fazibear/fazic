    pub fn left(&mut self) {
        if self.cursor.checked_sub(1).is_some() {
            self.cursor -= 1;
        }
    }

    pub fn right(&mut self) {
        if self.cursor + 1 <= 999 {
            self.cursor += 1;
        } else {
            self.cursor -= 39;
            self.shift();
        }
    }

    pub fn up(&mut self) {
        if self.cursor.checked_sub(40).is_some() {
            self.cursor -= 40;
        }
    }

    pub fn down(&mut self) {
        if self.cursor + 40 < 999 {
            self.cursor += 40;
        } else {
            self.shift();
        }
    }

    pub fn char(&mut self, char: char) {
        match self.char_index(char) {
            Some(_) => {
                self.chars[self.cursor] = char;
                self.right();
            },
            _ => ()
        }
    }

    pub fn char_index(&self, char: char) -> Option<usize>  {
        self.supported_chars
            .chars()
            .position(|x| x == char)
    }

    fn shift(&mut self) {
        let mut chars = ['\0'; 1000];
        for i in 40..1000 {
            chars[i - 40] = self.chars[i]
        }
        self.chars = chars;
    }
